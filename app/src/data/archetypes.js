// 每个角色基于其原型维度生成4维向量（D1洞察、D5创造、D2社交、D4稳定）
export function getAllCharacters() {
  const result = []
  const primaryKeys = ['D1', 'D5', 'D2', 'D4']
  for (const [key, base] of Object.entries(BASE)) {
    const traits = {}
    // 高低分目标放宽到 0.3 / 0.7，避免过于极端
    traits.D1 = key[0] === 'i' ? 0.70 : 0.30
    traits.D5 = key[1] === 'c' ? 0.70 : 0.30
    traits.D2 = key[2] === 'S' ? 0.70 : 0.30
    traits.D4 = key[3] === 'B' ? 0.70 : 0.30

    result.push({
      id: 'hero-' + key,
      name: base.name,
      prefix: base.prefix,
      img: base.img,
      verse: base.verse,
      desc: base.desc,
      traits,
      alignment: 'hero'
    })
    const villain = VILLAIN_MAP[base.name]
    if (villain) {
      result.push({
        id: 'villain-' + key,
        name: villain.name,
        prefix: villain.prefix,
        img: villain.img,
        verse: villain.verse,
        desc: villain.desc,
        traits,
        alignment: 'villain'
      })
    }
  }

  // 30 特殊人格：只有对应维度，其他维度为中性 0.5
  const specialList = [
    ...Object.entries(SPECIAL_HIGH).map(([dim, char]) => ({ dim, ...char, type: 'high' })),
    ...Object.entries(SPECIAL_LOW).map(([dim, char]) => ({ dim, ...char, type: 'low' }))
  ]
  specialList.forEach((char, i) => {
    const traits = {}
    primaryKeys.forEach(d => { traits[d] = 0.5 })
    traits[char.dim] = char.type === 'high' ? 0.95 : 0.05
    result.push({
      id: 'special-' + i,
      name: char.name,
      prefix: char.prefix,
      img: char.img,
      verse: char.verse,
      desc: char.desc,
      traits,
      alignment: 'special'
    })
  })

  return result
}
