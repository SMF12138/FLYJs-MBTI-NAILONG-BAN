function getSpecialTraits(scores) {
  const traits = []
  for (const dim of Object.keys(SPECIAL_HIGH)) {
    const v = scores[dim] !== undefined ? scores[dim] : 50
    if (v >= 95) {
      const similarity = Math.round(100 - (100 - v) / 95 * 100) // 越接近95分越高
      traits.push({ ...SPECIAL_HIGH[dim], dim, type: 'high', similarity: Math.max(0, Math.min(100, similarity)) })
    } else if (v <= 5) {
      const similarity = Math.round(100 - v / 95 * 100) // 越接近0分越高
      traits.push({ ...SPECIAL_LOW[dim], dim, type: 'low', similarity: Math.max(0, Math.min(100, similarity)) })
    }
  }
  return traits
}

// ─── 正邪判断（11维度加权）─���
function calcAlignment(scores) {
  const g = (d) => scores[d] !== undefined ? scores[d] : 50

  // 正派倾向：道德感驱动
  const hero = (
    g('D7')  * 0.25 +   // 恻隐（Batson）
    g('D9')  * 0.20 +   // 是非（Kohlberg）
    g('D10') * 0.20 +   // 羞恶（Haidt）
    g('D6')  * 0.10 +   // 荣辱（Benedict）
    g('D8')  * 0.10 +   // 信任（Bowlby）
    g('D11') * 0.05 +   // 勇敢（轻权重，勇敢≠善良）
    (100 - g('D14')) * 0.10  // 反贪婪
  )

  // 反派倾向：缺乏约束驱动（懦弱≠邪恶，D11不参与）
  const villain = (
    (100 - g('D7'))  * 0.20 +   // 缺乏共情
    (100 - g('D9'))  * 0.20 +   // 缺乏是非
    (100 - g('D10')) * 0.20 +   // 缺乏羞恶
    g('D14')         * 0.25 +   // 贪婪（反派核心）
    (100 - g('D8'))  * 0.10 +   // 多疑
    (100 - g('D6'))  * 0.05    // 无荣辱感
  )

  // 转百分比
  const total = hero + villain
  const heroPct = total > 0 ? Math.round((hero / total) * 100) : 50
  const villainPct = 100 - heroPct

  // 阈值判定
  let alignment = 'neutral'
  if (heroPct >= 60) alignment = 'hero'
  else if (heroPct <= 40) alignment = 'villain'

  return { alignment, heroPct, villainPct }
}

export function classifyArchetype(scores) {
  const primary = {
    insight: scores.D1 >= THRESHOLD,
    creative: scores.D5 >= THRESHOLD,
    social: scores.D2 >= THRESHOLD,
    stable: scores.D4 >= THRESHOLD,
  }

  const key = [
    primary.insight ? 'i' : 'e',  // 洞察/直觉
    primary.creative ? 'c' : 'r', // 创造/务实
    primary.social ? 'S' : 's',   // 合群/独处
    primary.stable ? 'B' : 'b',   // 稳定/波动
  ].join('')

  const base = BASE[key] || { name: '未知原型', emoji: '❓', verse: '' }
  const alignment = calcAlignment(scores)

  const heroName = base.name
  const heroImg = base.img
  const baseKey = key
  const villainData = VILLAIN_MAP[base.name] || { name: base.name + '(暗)', img: base.img }
  const villainName = villainData.name
  const villainImg = villainData.img
  const align = alignment.alignment
  const displayName = align === 'villain' ? villainName : heroName
  const displayEmoji = align === 'villain' ? '😈' : base.emoji
  const displayImg = align === 'villain' ? villainImg : heroImg
  const alignmentLabel = { hero: '正派', villain: '反派', neutral: '灰色' }[align]

  // 次维度标签
  const tags = []
  if ((scores.D3  || 0) >= THRESHOLD) tags.push('果断')
  if ((scores.D12 || 0) >= THRESHOLD) tags.push('恒心')
  if ((scores.D15 || 0) >= THRESHOLD) tags.push('幽默')
  if ((scores.D13 || 0) >= THRESHOLD) tags.push('洒脱')

  // 特殊人格（极端值触发）
  const specialTraits = getSpecialTraits(scores)

  return {
    name: displayName, emoji: displayEmoji, img: displayImg, verse: align === 'villain' ? villainData.verse : base.verse,
    desc: align === 'villain' ? (villainData.desc || '') : (base.desc || ''),
    key, baseKey, alignment: align, alignmentLabel, heroPct: alignment.heroPct, villainPct: alignment.villainPct,
    heroName, villainName, heroEmoji: base.emoji, villainEmoji: '😈', heroImg, villainImg,
    tags, specialTraits,
    traits: {
      D1: primary.insight ? 0.70 : 0.30,
      D5: primary.creative ? 0.70 : 0.30,
      D2: primary.social ? 0.70 : 0.30,
      D4: primary.stable ? 0.70 : 0.30,
    },
  }
}

export function getArchetypeDisplay(baseKey, alignment) {
  const base = BASE[baseKey]
  const villain = VILLAIN_MAP[base.name]
  if (alignment === 'villain' && villain) {
    return { verse: villain.verse, img: villain.img, name: villain.name, desc: villain.desc }
  }
  return { verse: base.verse, img: base.img, name: base.name, desc: base.desc }
}

